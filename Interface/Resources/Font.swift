import UIKit

public enum Font {
    public static let title: UIFont = .systemFont(ofSize: 24, weight: .semibold)
    public static let highlight: UIFont = .systemFont(ofSize: 20, weight: .semibold)
    public static let header: UIFont = .systemFont(ofSize: 17, weight: .bold)
    public static let body: UIFont = .systemFont(ofSize: 17, weight: .regular)
    public static let caption: UIFont = .systemFont(ofSize: 12, weight: .bold)
}
